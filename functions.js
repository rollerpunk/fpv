//------------------------------------------
// show/hide settings menu
//------------------------------------------
function settingsShow() {
 
  if (document.getElementById("setup").style.display == 'none' || "" )
  {
    document.getElementById("setup").style.display = "inline";  
  }
  else
  {
    document.getElementById("setup").style.display = 'none';
  }
};

